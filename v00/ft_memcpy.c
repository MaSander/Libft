#include <stddef.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((unsigned *)dest)[i] = ((unsigned *)src)[i];
		i++;
	}
	return (dest);
}
