char	*ft_strrchr(const char *s, int c)
{
	char	*last;

	last = ((char *) 0);
	while (*s)
	{
		if (*s == (unsigned char)c)
			last = (char *)s;
		s++;
	}
	if (*s == (unsigned char)c)
		return ((char *)s);
	return ((char *)last);
}
